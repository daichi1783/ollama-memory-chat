// ===== Core i18n functions =====

function getCurrentLang() {
  return localStorage.getItem('memoria_lang') || 'ja';
}

function setLang(lang) {
  if (!TRANSLATIONS[lang]) return;
  localStorage.setItem('memoria_lang', lang);
  applyTranslations();
  // 動的コンテンツの再描画（各ページで onLangChange を定義すれば呼ばれる）
  if (typeof onLangChange === 'function') onLangChange();
}

function t(key) {
  const lang = getCurrentLang();
  return TRANSLATIONS[lang]?.[key] ?? TRANSLATIONS['en']?.[key] ?? key;
}

function applyTranslations() {
  // data-i18n: textContent（innerHTMLを含む要素はスキップしてtextContentのみ）
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = t(el.dataset.i18n);
    if (val !== el.dataset.i18n) el.textContent = val;
  });
  // data-i18n-placeholder: placeholder attribute
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  // data-i18n-title: title attribute
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
  // data-i18n on <option>: translate option text
  document.querySelectorAll('option[data-i18n]').forEach(el => {
    const val = t(el.dataset.i18n);
    if (val !== el.dataset.i18n) el.textContent = val;
  });
  // Update html lang attribute
  const langMap = { ja: 'ja', en: 'en', es: 'es' };
  document.documentElement.lang = langMap[getCurrentLang()] || 'ja';
}

// Auto-apply on DOM ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', applyTranslations);
} else {
  applyTranslations();
}
